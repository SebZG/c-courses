/*

printf() function is used in a C program to display any value like float,
integer, character, string, etc on the console screen.
It is a pre-defined function that is already declared in the stdio.h(header file).

*/

#include <stdio.h>

int main()
{
    // Creating an int type variable
    int a = 20;

    // Printing the value of a variable
    printf("%d\n", a);

    // Printing a string
    printf("This is a string");

    return 0;
}
