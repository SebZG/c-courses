#include <stdio.h>

int main()
{
    printf("Hello World \n");     // \n means new line
    printf("Hel\tlo World \n");   // \t means tab
    printf("Hello \rWorld \n");   // \r means return
    printf("Hel\blo World \n");   // \b means backspace
    printf("Hello \a World \n");  // \a means alert/bell

    return 0;
}