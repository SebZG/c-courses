#include <stdio.h>
#include <stdlib.h>

int main ()
{
    int a;
    int b;

    char niceArray[9][9] = {{' ',' ','=',' ',' ',' ',' ',' '},
                            {' ',' ','=','=',' ',' ',' ',' '},
                            {' ',' ','=','=','=',' ',' ',' '},
                            {'=','=','=','=','=','=',' ',' '},
                            {'=','=','=','=','=','=','=',' '},
                            {'=','=','=','=','=','=',' ',' '},
                            {' ',' ','=','=','=',' ',' ',' '},
                            {' ',' ','=','=',' ',' ',' ',' '},
                            {' ',' ','=',' ',' ',' ',' ',' '}};

    for(a = 0; a<9; a++)
    {
        printf("\n");
        for(b = 0; b<9; b++)
        {
            printf("%c  ", niceArray[a][b]);
        }
    }

    printf("\n\n\n");

    for(a = 0; a<9; a++)
    {
        printf("\n");
        for(b = 0; b<9; b++)
        {
            printf("[%d][%d] in %x\n",a , b, &niceArray[a][b]);
        }
    }

    printf("\n\n\n");

    return 0;
}

